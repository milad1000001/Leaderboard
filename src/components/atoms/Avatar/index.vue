<template>
    <div
        :class="['m-auto',{
            'top':variation==='top',
            'list':variation==='list'
        }]"
    >
        <img
            class="block rounded-circle"
        >
    </div>
</template>

<script>

export default {
  name: 'Avatar',
  props: {
    variation: {
      type: String,
    },
    personUsername: {
      type: String,
      required: true,
    },
  },
  created() {
    this.getPersonImageFrom(this.personUsername);
  },
  methods: {
    getPersonImageFrom(personUsername) {
      this.$store.dispatch('ranking/GetPersonPhoto', personUsername)
        .then((response) => response);
    },
  },
};
</script>

<style scoped>
.top{
  max-width: theme('spacing.24');
  max-height: theme('spacing.24');
}
.list{
  max-width: theme('spacing.12');
  max-height: theme('spacing.12');
}
</style>
